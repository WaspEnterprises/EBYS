package ebys;

public class Not {
	
	private int not;
	private String type;
	
	public Not(String type,int not){
		this.type=type;
		this.not=not;
		
	}
	public int getNot() {
		return not;
	}
	public void setNot(int not) {
		this.not = not;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
}
